var colors = ["Rouge", "Vert", "Jaune"];

// Méthode Join
console.log(colors.join('-'));

// Méthode Push
colors.push("Orange");
console.log(colors);

// Méthode unshift
colors.unshift("Bleue");
console.log(colors);

// Méthode reverse
colors.reverse();
console.log(colors);

